const SCALE_FACTORS = {
  distance: 1 / 1e7,
  radius: 1 / 5000,
  time: 0.0001
};

function getScaledPlanetData() {
  const scaled = {};
  Object.entries(PLANETS_DATA).forEach(([key, planet]) => {
    scaled[key] = {
      ...planet,
      scaledRadius: planet.radius * SCALE_FACTORS.radius,
      scaledDistance: planet.distance * SCALE_FACTORS.distance
    };
  });
  return scaled;
}
